from tqdm import tqdm


def parseInput(inp):
    inp = inp.split("\n\n")
    keys = []
    locks = []
    for lockKey in inp:
        lines = lockKey.split("\n")
        counts = [0] * len(lines[0])
        for i, line in enumerate(lines):
            if i == 0 or i == len(lines) - 1:
                continue
            for j, char in enumerate(line):
                if char == "#":
                    counts[j] += 1
        if lines[0][0] == ".":
            keys.append(counts)
        else:
            locks.append(counts)
    return keys, locks


def fits(key, lock, maxVal=5):
    return max([x + y for (x, y) in zip(key, lock)]) <= maxVal


def solution1(inp):
    keys, locks = parseInput(inp)
    combs = set()
    for key in tqdm(keys):
        for lock in locks:
            if fits(key, lock):
                combs.add((tuple(key), tuple(lock)))
    return len(combs)


def solution2(inp):
    ans = 0
    inp = parseInput(inp)
    return ans


