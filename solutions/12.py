def getNumSides(perim):
    ans = 0
    for dir in perim:
        dirSeen = set()
        for dirLoc in perim[dir]:
            if dirLoc in dirSeen:
                continue
            ans += 1
            toExplore = deque([dirLoc])
            while toExplore:
                loc = toExplore.pop()
                if loc in dirSeen:
                    continue
                dirSeen.add(loc)
                perpendicularDir = ((dir[0] + 1) % 2, (dir[1] + 1) % 2)
                for dirExpl in [
                    perpendicularDir,
                    (-1 * perpendicularDir[0], -1 * perpendicularDir[1]),
                ]:
                    newLoc = (loc[0] + dirExpl[0], loc[1] + dirExpl[1])
                    if newLoc in perim[dir]:
                        toExplore.append(newLoc)
    return ans


