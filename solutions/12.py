from collections import defaultdict, deque
from util import dirs


def parseInput(inp):
    inp = [list(x) for x in inp.split("\n")]
    return inp


def findRegion(grid, start):
    # start is (row, col)
    pos = set()
    perimeter = 0
    plant = grid[start[0]][start[1]]
    toExplore = deque([start])
    while toExplore:
        loc = toExplore.pop()
        if loc in pos:
            continue
        pos.add(loc)
        for dir in dirs():
            dr, dc = dir
            newLoc = (loc[0] + dr, loc[1] + dc)
            if 0 <= newLoc[0] < len(grid) and 0 <= newLoc[1] < len(grid[1]):
                if grid[newLoc[0]][newLoc[1]] == plant:
                    toExplore.append(newLoc)
                else:
                    perimeter += 1
            else:
                perimeter += 1
    return pos, perimeter


