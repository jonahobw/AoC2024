from tqdm import tqdm


def parseInput(inp):
    inp = inp.split("\n")
    s = (0, 0)
    e = (0, 0)
    path = []
    inp = [list(x) for x in inp]
    for r, row in enumerate(inp):
        for c, char in enumerate(row):
            if char != "#":
                path.append((r, c))
            if char == "S":
                s = (r, c)
            if char == "E":
                e = (r, c)
    return inp, s, e, path


def findPath(grid, s, e):
    dirs = [(1, 0), (0, -1), (-1, 0), (0, 1)]
    path = []
    visited = set()
    pathlength = 0
    current = s
    while current != e:
        path.append(current)
        visited.add(current)
        pathlength += 1
        neighbors = [(current[0] + dr, current[1] + dc) for dr, dc in dirs]
        validNeighbor = False
        for neighbor in neighbors:
            if neighbor not in visited and grid[neighbor[0]][neighbor[1]] in [".", "E"]:
                current = neighbor
                validNeighbor = True
                break
        if not validNeighbor:
            # no path
            return []
    path.append(current)
    return path


def solution1(inp, maxSkipTime=1, minTimeSaved=100):
    ans = 0
    grid, s, e, _ = parseInput(inp)
    path = findPath(grid, s, e)  # grid loc (row, col) in a list from start to end

    for i in tqdm(range(len(path) - minTimeSaved)):
        for j in range(i + minTimeSaved, len(path)):
            start = path[i]
            end = path[j]
            distance = abs(start[0] - end[0]) + abs(start[1] - end[1])
            if distance > maxSkipTime:
                continue
            timeSaved = j - i - distance
            if timeSaved >= minTimeSaved:
                ans += 1
    return ans


